    private void tabelaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaClienteMouseClicked
        // TODO add your handling code here:
        if(tabelaCliente.getRowCount() > 0){
            //Verifico se o usuário selecionou alguma linha (Primeira linha = 0)
            if(tabelaCliente.getSelectedRow() >= 0){
                //Atribuo os valores que estão na linha selecionada para a tabela
                Cliente cliente = new Cliente();
                
                cliente = ClienteController.getClienteByID(Integer.parseInt(tabelaCliente.getModel().getValueAt(tabelaCliente.getSelectedRow(), 0).toString()));
                idDoCliente = Integer.parseInt(tabelaCliente.getModel().getValueAt(tabelaCliente.getSelectedRow(), 0).toString());
                
                nomeTxt2.setText(cliente.getNome());
                cpfTxt2.setText(cliente.getCpf());
                
                if(cliente.getGenero().equals("Masculino")){
                   mascRadioButton2.setSelected(true); 
                   femRadioButton2.setEnabled(false);
                }else{
                    femRadioButton2.setSelected(true);
                    mascRadioButton2.setEnabled(false);
                }
                
                dataNascTxt2.setText(cliente.getDataNasc());
                estadoCivilComboBox2.setSelectedItem(cliente.getEstadoCivil());
                ufComboBox2.setSelectedItem(cliente.getUF());
                enderecoTxt2.setText(cliente.getEndereco());
                numeroTxt2.setText(String.valueOf(cliente.getNumeroResidencia()));
                numeroTxt2.setText(String.valueOf(cliente.getNumeroResidencia()));
                cepTxt2.setText(String.valueOf(cliente.getCep()));
                bairroTxt2.setText(cliente.getBairro());
                tipoEnderecoComboBox2.setSelectedItem(cliente.getTipoEndereco());
                complementoTxt2.setText(cliente.getComplemento());
                cidadeTxt2.setText(cliente.getCidade());
                emailTxt2.setText(cliente.getEmail());
                telefoneTxt2.setText(String.valueOf(cliente.getTelefone()));
            }
        }
    }//GEN-LAST:event_tabelaClienteMouseClicked

