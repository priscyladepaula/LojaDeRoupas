    public void LoadTable(){
        ArrayList<String[]> linhasClientes = ClienteController.getClientes();
        
        DefaultTableModel tmClientes = new DefaultTableModel();
        tmClientes.addColumn("ID");
        tmClientes.addColumn("CLIENTE");
        tabelaCliente.setModel(tmClientes);
        
        for(String[] c:linhasClientes){
            tmClientes.addRow(c);
        }
        
        tabelaCliente.getColumnModel().getColumn(0).setPreferredWidth(50); //ID
        tabelaCliente.getColumnModel().getColumn(1).setPreferredWidth(300);
        
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        
        tabelaCliente.getColumnModel().getColumn(0).setCellRenderer(centralizado); //ID
        tabelaCliente.getColumnModel().getColumn(1).setCellRenderer(centralizado);
    }
    
