    private void deleteButtpnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtpnActionPerformed
        // TODO add your handling code here:
        if(tabelaProduto.getRowCount() > 0){
            int numeroLinha = tabelaProduto.getSelectedRow();
            if(ProdutoController.Excluir(Integer.parseInt(tabelaProduto.getValueAt(numeroLinha,0).toString()))){
                this.LoadTable();
                JOptionPane.showMessageDialog(this,"Produto excluído da base de dados");
            }else{
                JOptionPane.showMessageDialog(this,"Falha ao excluir o produto!");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Não há produtos para excluir!");
        }
    }//GEN-LAST:event_deleteButtpnActionPerformed

    private void tabelaProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdutoMouseClicked
        // TODO add your handling code here:
        if(tabelaProduto.getRowCount()>0){
            //Verifico se o usuário selecionou alguma linha (Primeira linha = 0)
            if(tabelaProduto.getSelectedRow()>=0){
                //Variável acessória para identifcar se o formulário está em modo de edição ou alteração
                modoTela = "Editar";

                //Atribuo os valores que estão na linha selecionada para a tabela
                descricaoTxt.setText(tabelaProduto.getModel().getValueAt(tabelaProduto.getSelectedRow(), 1).toString());
                tamanhoComboBox.setSelectedItem(tabelaProduto.getModel().getValueAt(tabelaProduto.getSelectedRow(), 2).toString());
                corComboBox.setSelectedItem(tabelaProduto.getModel().getValueAt(tabelaProduto.getSelectedRow(), 3).toString());
                //TODO: pegar a seleção do genero feminino ou masculino                
                if(tabelaProduto.getModel().getValueAt(tabelaProduto.getSelectedRow(), 4).toString().equals("Masculino")){
                   mascRadioButton.setSelected(true); 
                   femRadioButton.setEnabled(false);
                }else{
                    femRadioButton.setSelected(true);
                    mascRadioButton.setEnabled(false);
                }
                quantidadeTxt.setText(tabelaProduto.getModel().getValueAt(tabelaProduto.getSelectedRow(), 5).toString());
                vlrUnitarioTxt.setText(tabelaProduto.getModel().getValueAt(tabelaProduto.getSelectedRow(), 6).toString());
                codProduto = Integer.parseInt(tabelaProduto.getModel().getValueAt(tabelaProduto.getSelectedRow(), 0).toString());
                
            }
            else{
                JOptionPane.showMessageDialog(this,"Selecione um produto para editar!");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Não há produto para editar!");
        }
    }//GEN-LAST:event_tabelaProdutoMouseClicked

