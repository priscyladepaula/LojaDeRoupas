    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        ValidarCampos();
        if(modoTela.equals("Criar")){
            if(ProdutoController.Salvar(descricaoTxt.getText(), tamanhoComboBox.getSelectedItem().toString(), corComboBox.getSelectedItem().toString(), 
                                        generoGroup.getSelection().getActionCommand(), Integer.parseInt(quantidadeTxt.getText()), Double.parseDouble(vlrUnitarioTxt.getText()))){
                
                this.LoadTable();
                JOptionPane.showMessageDialog(null,"Produto cadastrado com sucesso!");
                LimparFormulario();
            } else{
                JOptionPane.showMessageDialog(null,"Falha ao cadastrar produto!");
            }
        } else{
            if(ProdutoController.Atualizar(descricaoTxt.getText(), tamanhoComboBox.getSelectedItem().toString(), corComboBox.getSelectedItem().toString(), 
                    generoGroup.getSelection().getActionCommand(), Integer.parseInt(quantidadeTxt.getText()), Double.parseDouble(vlrUnitarioTxt.getText()), codProduto)){
                
                this.LoadTable();
                JOptionPane.showMessageDialog(null,"Produto atualizado com sucesso!");
                LimparFormulario();
            } else{
                JOptionPane.showMessageDialog(null,"Falha ao atualizar produto!");
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

