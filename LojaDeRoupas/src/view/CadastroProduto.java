    public void HabilitarFormulario(){
        descricaoTxt.setEditable(true);
        tamanhoComboBox.setEnabled(true);
        corComboBox.setEnabled(true);
        femRadioButton.setEnabled(true);
        mascRadioButton.setEnabled(true);
        quantidadeTxt.setEnabled(true);
        vlrUnitarioTxt.setEditable(true);
    }

    public void DesabilitarFormulario(){
        descricaoTxt.setEditable(false);
        tamanhoComboBox.setEnabled(false);
        corComboBox.setEnabled(false);
        femRadioButton.setEnabled(false);
        mascRadioButton.setEnabled(false);
        quantidadeTxt.setEnabled(false);
        vlrUnitarioTxt.setEditable(false);
    }
    
